            <div class="w3-code notranslate htmlHigh">
   <span class="tagnamecolor" style="color:brown"><span class="tagcolor" style="color:mediumblue">&lt;</span>span<span class="attributecolor" style="color:red"> onclick<span class="attributevaluecolor" style="color:mediumblue">="this.parentElement.style.display='none'"</span><br>class<span class="attributevaluecolor" style="color:mediumblue">="w3-button
   w3-display-topleft"</span></span><span class="tagcolor" style="color:mediumblue">&gt;</span></span>&amp;times;<span class="tagnamecolor" style="color:brown"><span class="tagcolor" style="color:mediumblue">&lt;</span>/span<span class="tagcolor" style="color:mediumblue">&gt;</span></span>  </div>
