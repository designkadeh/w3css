    <h2>W3.CSS Responsive Classes</h2>
    <p>W3.CSS's grid system is responsive, and the columns will re-arrange automatically depending on the screen size:</p>
    <table class="w3-table w3-bordered w3-striped w3-border">
        <tbody><tr>
            <th style="width:30%">Class</th>
